package ProblemaCaballo;

/**
 *
 * @author guija
 */
public class coordenadas{
    private int coordX;
    private int coordY;
    
    public coordenadas(){
        this.coordX = 0;
        this.coordY = 0;
    }
    public int getCoordX() {
        return coordX;
    }
    public void setCoordX(int coordX) {
        this.coordX = coordX;
    }
    public int getCoordY() {
        return coordY;
    }
    public void setCoordY(int coordY) {
        this.coordY = coordY;
    }
}

